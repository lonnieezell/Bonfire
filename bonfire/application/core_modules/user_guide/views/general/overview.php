<h1>What is Bonfire?</h1>

<p>Bonfire is here to make your job easier and faster. By forming a powerful base--including templating, user management, role-based permissions and powerful developer tools--it provides a great springboard for your next CodeIgniter-based project. It's big-picture goals are to:</p>

<ul>
	<li>Provide a powerful base for your next project,</li>
	<li>Provide tools that make creating the app itself simpler and more enjoyable,</li>
	<li>Provide a good-looking admin area that doesn't suck, and that you're proud to present to your client,</li>
	<li>Provide a flexible parent/child template system that makes implementing your client's design a breeze,</li>
	<li>and do this all while keeping the performance as high as possible.</li>
</ul>